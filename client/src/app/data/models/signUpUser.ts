/**
 * Model used for signing up new users
 */
export class SignUpUser {
	/**
	 * Username of the new user
	 */
	username: string;

	/**
	 * Email of the new user
	 */
	email: string;

	/**
	 * Mobile phone number of the new user
	 */
	phone: string;

	/**
	 * Plain text password of the new user
	 */
	password: string;
}
